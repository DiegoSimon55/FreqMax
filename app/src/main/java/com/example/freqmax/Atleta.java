package com.example.freqmax;

public class Atleta {
    private String nome;
    private int idade;
    private int fcm;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }

    public int getFCM() {
        return fcm;
    }

    public void setFCM(int fcm) {
        this.fcm = fcm;
    }
}
